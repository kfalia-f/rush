int		ft_box(int **arr, int start_i, int start_j, int n)
{
	int i;
	int j;

	i = 0;
	while (i < 3)
	{
		j = 0;
		while (j < 3)
		{
			if (arr[i + start_i][j + start_j] == n)
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
