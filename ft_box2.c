int		ft_box2(int **arr, int start_i, int start_j, int n)
{
	int i;
	int j;
	int k;

	i = 0;
	k = 0;
	while (i < 3)
	{
		j = 0;
		while (j < 3)
		{
			if (arr[i + start_i][j + start_j] == n)
				k++;
			j++;
		}
		i++;
	}
	if (k == 1)
		return (1);
	else
		return (0);
}
