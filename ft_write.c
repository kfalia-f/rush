void	ft_putchar(char c);

void	ft_write(int **arr)
{
	int		i;
	int		j;
	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			ft_putchar(arr[i][j] + '0');
			if (j != 8)
				ft_putchar(' ');
			j++;
		}
		i++;
		ft_putchar('\n');
	}
}
