int		ft_find(int **arr, int *i, int *j)
{
	*i = 0;
	while (*i < 9)
	{
		*j = 0;
		while (*j < 9)
		{
			if (arr[*i][*j] == 0)
				return (1);
			*j = *j + 1;
		}
		*i = *i + 1;
	}
	return (0);
}
