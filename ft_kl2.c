int		ft_kl2(int argc, char **argv)
{
	int		i;
	int		j;
	int		k;

	i = 1;
	k = 0;
	while (i < argc)
	{
		j = 0;
		while (argv[i][j] != '\0')
		{
			if (argv[i][j] != '.')
				if ((argv[i][j] < '0') || (argv[i][j] > '9'))
					k++;
			j++;
		}
		i++;
	}
	if (k != 0)
		return (0);
	return (1);
}
