int		ft_stroka2(int **arr, int i, int j, int n)
{
	int k;
	int l;

	k = 0;
	l = 0;
	while (k < 9)
	{
		if (arr[i][k] == n || arr[k][j] == n)
			l++;
		k++;
	}
	if (l == 1 || l == 2)
		return (1);
	else
		return (0);
}
